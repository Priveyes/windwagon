package com.windwagon.broceliande.knights.entities;

import java.util.Comparator;
import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

@Entity
@Table( name = "ww_cycle" )
public class Cycle implements Comparable<Cycle> {

    public static final Comparator<Cycle> COMPARATOR =
            Comparator.comparing( Cycle::getCreation, Comparator.nullsFirst( Date::compareTo ) );

    @Id
    @SequenceGenerator( name = "sequence_id", sequenceName = "ww_sequence_id" )
    @GeneratedValue( strategy = GenerationType.SEQUENCE, generator = "sequence_id" )
    private Long id;

    @Temporal( TemporalType.DATE )
    private Date creation;

    @Lob
    private String description;

    @Override
    public int compareTo( Cycle cycle ) {
        return COMPARATOR.compare( this, cycle );
    }

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId( Long id ) {
        this.id = id;
    }

    /**
     * @return the creation
     */
    public Date getCreation() {
        return creation;
    }

    /**
     * @param creation the creation to set
     */
    public void setCreation( Date creation ) {
        this.creation = creation;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription( String description ) {
        this.description = description;
    }

}
